    private void dafSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dafSubmitActionPerformed
        

        boolean cek = false;
        if(dafAkun.getText().isEmpty() && dafPas.getText().isEmpty() && saldoUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukan nomor rek, pin, dan saldo terlebih dahulu", "GAGAL", JOptionPane.ERROR_MESSAGE);
        } else if (dafAkun.getText().isEmpty() && !dafPas.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukan nomor rek terlebih dahulu", "GAGAL", JOptionPane.ERROR_MESSAGE);
        } else if (dafPas.getText().isEmpty() && !dafAkun.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukan nomor pin terlebih dahulu", "GAGAL", JOptionPane.ERROR_MESSAGE);
        } else if(dafAkun.getText().isEmpty() && dafPas.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukan nomor pin dan nomor rekening terlebih dahulu", "GAGAL", JOptionPane.ERROR_MESSAGE);
        }else if(dafAkun.getText().isEmpty() || dafPas.getText().isEmpty() || saldoUser.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Terdapat bagian yang belum anda isi", "GAGAL", JOptionPane.ERROR_MESSAGE);
        }else if(!pria.isSelected() && !wanita.isSelected()){
            JOptionPane.showMessageDialog(null, "Pilih jenis kelamin anda", "GAGAL", JOptionPane.ERROR_MESSAGE);
        }else if(Integer.parseInt(saldoUser.getText())<0){
            JOptionPane.showMessageDialog(null, "Saldo yang anda masukan harus lebih dari 0", "GAGAL", JOptionPane.ERROR_MESSAGE);
        }
        else{
        String noRek = dafAkun.getText();
        String noPin = dafPas.getText();
        String saldoPengguna = dafAkun.getText();
        int uang = Integer.parseInt(saldoUser.getText());
            for (String i : akun.keySet()) {
                if (i.equals(noRek)) {
                    cek = true;
                    break;
                }
            }
            if (cek == true) {
                JOptionPane.showMessageDialog(null, "Akun telah ada", "GAGAL", JOptionPane.ERROR_MESSAGE);
            } else {
                akun.put(noRek, noPin);
                // saldo
                userSaldo.put(saldoPengguna, uang);
                JOptionPane.showMessageDialog(null, "Akun berhasil terdaftar", "BERHASIl", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        dafAkun.setText("");
        dafPas.setText("");
        saldoUser.setText("");
    }//GEN-LAST:event_dafSubmitActionPerformed

    private void logSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logSubmitActionPerformed
        String noRekLog = logRek.getText();
        String noPinLog = logPas.getText();
        boolean kondisi = false;
        for (String i : akun.keySet()) {
            if (noRekLog.equals(i) && noPinLog.equals(akun.get(i))) {
                penanda = i;
                kondisi = true;
                break;
            }
        }
        if (kondisi == true) {
            JOptionPane.showMessageDialog(null, "Login berhasil", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
            butDaftar.setEnabled(false);
            butLogin.setEnabled(false);
            butDeposit.setEnabled(true);
            butLogout.setEnabled(true);
            butInfo.setEnabled(true);
            butTarik.setEnabled(true);
            butTransfer.setEnabled(true);
            panelMenu.removeAll();
            panelMenu.repaint();
            panelMenu.add(suksesLogin);
            panelMenu.revalidate();
        } else {
            JOptionPane.showMessageDialog(null, "Tidak berhasil Login", "GAGAL", JOptionPane.ERROR_MESSAGE);
        }
        logRek.setText("");
        logPas.setText("");


    }//GEN-LAST:event_logSubmitActionPerformed

