    private void prosesTransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesTransferActionPerformed
        int uangDidapat = Integer.parseInt(saldoTF.getText());
        String tujuan = rekTujuan.getText();
        if(saldoTF.getText().isEmpty() && rekTujuan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukan saldo dan rekening tujuan","Error", JOptionPane.ERROR_MESSAGE);
        }else if(saldoTF.getText().isEmpty() || rekTujuan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukan saldo atau rekening tujuan","Error", JOptionPane.ERROR_MESSAGE);
        }else if(uangDidapat<0){
            JOptionPane.showMessageDialog(null, "Saldo tidak boleh minus","Error", JOptionPane.ERROR_MESSAGE);
        }else if(!userSaldo.containsKey(tujuan)){
            JOptionPane.showMessageDialog(null, "Rekening tujuan tidak tersedia","Error", JOptionPane.ERROR_MESSAGE);
        }else{
            userSaldo.put(tujuan, userSaldo.get(tujuan) + uangDidapat);
            userSaldo.put(penanda, userSaldo.get(penanda) - uangDidapat);
            JOptionPane.showMessageDialog(null, "Uang berhasil ditransfer", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
        }
        
        rekTujuan.setText("");
        saldoTF.setText("");
    }//GEN-LAST:event_prosesTransferActionPerformed

