    private void prosesTarikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesTarikActionPerformed
        int uangDidapat = Integer.parseInt(tarikSaldo.getText());
        if(tarikSaldo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukan saldo","Error", JOptionPane.ERROR_MESSAGE);
        }else if(uangDidapat<0){
            JOptionPane.showMessageDialog(null, "Saldo tidak boleh minus","Error", JOptionPane.ERROR_MESSAGE);
        }else{
            for (String i : userSaldo.keySet()) {
            if (i.equals(penanda)) {
                int saldoSebelum = userSaldo.get(i);
                int saldoNow = saldoSebelum - uangDidapat;
                if (saldoNow >= 0) {
                    userSaldo.put(i, saldoNow);
                    JOptionPane.showMessageDialog(null, "Uang berhasil ditarik", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Saldo anda tidak mecukupi", "GAGAL", JOptionPane.INFORMATION_MESSAGE);
                }
                break;
            }
        }
        }
        tarikSaldo.setText("");
    }//GEN-LAST:event_prosesTarikActionPerformed

