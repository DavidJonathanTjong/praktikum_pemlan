    private void prosesDepoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesDepoActionPerformed
        int uangDidapat = Integer.parseInt(depoUang.getText());
        if(depoUang.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukan saldo","Error", JOptionPane.ERROR_MESSAGE);
        }else if(uangDidapat<0){
            JOptionPane.showMessageDialog(null, "Saldo tidak boleh minus","Error", JOptionPane.ERROR_MESSAGE);
        }else{
           for (String i : userSaldo.keySet()) {
            if (i.equals(penanda)) {
                int saldoSebelum = userSaldo.get(i);
                int saldoNow = hitunganDepo(saldoSebelum, uangDidapat);
                userSaldo.put(i, saldoNow);
                JOptionPane.showMessageDialog(null, "Uang berhasil dikirim", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                break;
            }
        } 
        }
        depoUang.setText("");
    }//GEN-LAST:event_prosesDepoActionPerformed

